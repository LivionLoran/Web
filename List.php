                    Увидеть красоту грузинских гор благодаря экскурсии Ананури-Гудаури – Казбеги.
                    Познать зеленую красоту Грузии, Вы сможете на экскурсии Боржоми - Бакуриани (сможете попить целебной воды из источника Боржоми).
                    История Грузии откроется перед Вами на экскурсии Гори (музей Й.В. Сталина) и Уплисцихе - каменный город.
                    Экскурсия в винный регион Грузии и город любви Сигнахи, с посещением Бодбе.</br>
