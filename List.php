                    Прогулка по местам, которые дышать вечностью и являются гордостью архитектуры столицы гордой Грузии:
                    Кафедральный собор «Самеба», самый большой православный храм в мире. Зрелищно, красиво, удивительно. Сразу же ощущается величие замысла архитекторов.
                    Еще один символ города собор «Метехи» (XIII в) - древняя цитадель и резиденция грузинских царей. По преданию, здесь молилась сама царица Тамара.
                    Посетим так же сложнейшее архитектурное творение итальянского архитектора, лицо современного Тбилиси – Мост Мира.
                    Внимание мы так же уделим Сионскому кафедральному собору. Здесь его ласково называют Сиони, расположен он в живописном месте, на берегу реки Куры. Скромный снаружи, он обладает чудотворной силой.
                    На подъемниках мы поднимемся к крепости Нарикала ("Неприступная крепость") - душа Тбилиси, с нее открывается захватывающая дух панорама города. Эта цитадель почти так же стара, как и сам город, ее относят к IV в. н. э.
                    Далее экскурсия во «Мцхета» – древнейший, город, первая столица Грузии, душа этой удивительной страны. Здесь у Вас будет возможность ознакомится с:
                    Кафедральным собором «Светицховели» (XI в). Он же, собор 12-ти апостолов. В основании его покоится Хитон Господень. Благодаря этой святыне Мцхета называют «вторым Иерусалимом».
                    Факультативно (25 долл США с человека) Здесь во Мцхета, мы посетим грузинскую крестьянскую семью. Вы узнаете, что такое настоящее грузинское гостеприимство. Здесь проведем дегустацию грузинских вин и чачи, узнаем историю виноградной культуры и виноделия из первых уст. Дегустация проводится из глиняных пиал.
                    Так же здесь Вас ждут мастер-классы грузинской кухни (ароматный хлеб в тоне и «грузинский сникерс» - чурчхела) и невероятно вкусный грузинский обед.
                    Поднимемся в монастырь Джвари (V в), откуда открывается прекрасная панорама древней столицы (в Иерусалиме есть церковь с таким же названием). По легенде, этот храм был воздвигнут над источавшим благовонное масло (миро) пнём того кедра, под которым молилась просветительница Грузии Святая Нино.
                    Возвращение в Тбилиси.
